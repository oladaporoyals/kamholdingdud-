Reuse and Recycle Scrap consists of recyclable materials, usually metals, left over from product manufacturing and consumption, such as parts of vehicles, building supplies, and surplus materials. Unlike waste, scrap has monetary value, especially recovered metals, and non-metallic materials are also recovered for recycling. Once collected, the materials are sorted into types — typically metal scrap will be crushed, shredded, and sorted using mechanical processes.<br/>
Recycling conserves resources, energy, and water while lowering greenhouse gas emissions. Additionally, it creates a new industrial structure and jobs.<br/> There is little question that the advantages of recycling our waste outweigh the straightforward, regular activity required.<br/>
